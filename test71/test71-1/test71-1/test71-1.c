//文件缓冲区
//ANSIC标准采用"缓冲文件系统"处理的数据文件的, 所谓缓冲文件系统是指系统自动地在内存中为程序中每一个正在
//使用的文件开辟一块"文件缓冲区'。从内存向磁盘输出数据会先送到内存中的缓冲区，装满缓冲区后才一起送到磁盘上。
//如果从磁盘向计算机读入数据，则从磁盘文件中读取数据输入到内存缓冲区（充满缓冲区），
//然后再从缓冲区逐个地将数据送到程序数据区（程序变量等),缓冲区的大小根据C编译系统决定的。


//文件指针
//缓冲文件系统中，关键的概念是“文件类型指针”，简称文件指针。
//每个被使用的文件都在内存中开辟了一个相应的文件信息区，用来存放文件的相关信息(如文件的名字，文件状态及文件当前的位置等）。
//这些信息是保存在一个结构体变量中的。该结构体类型是有系统声明的，取名FILE.
//例如 VS2008编译环境提供的stdio.h头文件中有以下的文件类型申明:
//不同的C编译器的FILE类型包含的内容不完全相同, 但是大同小异。
//每当打开一个文件的时候，系统会根据文件的情况自动创建一个FILE结构的变量，并填充其中信息，使用者不必关心细节。
//一般都是通过一个FILE的指针来维护这个FILE结构的变量，这样使用起来更加方便。
//下面我们可以创建―个FILE*的指针变量:
FILE* pf;//文件指针变量
//定义pf是一个指向FILE类型数据的指针变量。可以使pf指向某个文件的文件信息区
//(是一个结构体变量)。通过该文件信息区中的信息就能够访问该文件。
//也就是说，通过文件指针变量能够找到与它关联的文件。

